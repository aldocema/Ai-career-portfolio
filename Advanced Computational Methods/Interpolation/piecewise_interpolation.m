n = length(x);

plot(x, y, 'bo', 'Linewidth', 3)
%axis([x(1) x(n) y(n) y(1)])
%axis([0 14 -2 8])
hold on;
m = zeros(length(x));
for k = 1:n-1
    m(k) = (y(k+1) - y(k)) / (x(k+1) - x(k));
    x_aux = x(k):0.01:x(k+1);
    f_xx = y(k) + m(k) * (x_aux - x(k));     
    plot(x_aux, f_xx, 'r');
    grid on;
    pause;
