x = [0.9, 1.3, 1.9, 2.1, 2.6, 3.0, 3.9, 4.4, 4.7, 5.0, 6.0, 7.0, 8.0, ...
    9.2, 10.5, 11.3, 11.6, 12.0, 12.6, 13.0, 13.3];
